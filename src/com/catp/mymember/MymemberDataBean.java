package com.catp.mymember;

public class MymemberDataBean {
	private String userid;
	private String password;
	private String username;
	private String email;
	private String phone;
	private String zipcode;
	private String address1;
	private String address2;
	private String regdate;
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getAddress1() {
		return address1;
	}
	public void setAddress1(String address1) {
		this.address1 = address1;
	}
	public String getAddress2() {
		return address2;
	}
	public void setAddress2(String address2) {
		this.address2 = address2;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	
	@Override
	public String toString() {
		return "MymemberDataBean [userid=" + userid + ", password=" + password + ", username=" + username + ", email="
				+ email + ", phone=" + phone + ", zipcode=" + zipcode + ", address1=" + address1 + ", address2="
				+ address2 + ", regdate=" + regdate + "]";
	}
	
}
